//在8*8的棋盘上，放置8个皇后，使两两之间互不攻击。
//逐行放置皇后
//安全条件数组：L：右斜线，R：左斜线，C：列；
//q为皇后位置；

#include <iostream>
using namespace std;

int num=0;
int Q[9];
bool C[9];
bool R[17];
bool L[17];

void Try(int i){
    for(int j=1;j<=9;j++){
        bool right=C[j]&&L[i-j+9]&&R[i+j];
        if(!right){}
        else{
            Q[i]=j;
            C[j]=false;
            L[i-j+9]=false;
            R[i+j]=false;
            if(i<8){
                Try(i+1);
            }
            else if(i==8){
                for(int g=1;g<=8;g++){
                    cout<<Q[g]<<" ";
                }
                cout<<endl;
                num++;
            }
            Q[i]=0;
            C[j]= true;
            L[i-j+9]= true;
            R[i+j]= true;
        }
    }
}

int main() {
    for(int y=1;y<=8;y++){
        C[y]= true;
    }
    for(int z=2;z<=16;z++){
        R[z]= true;
        L[z]= true;
    }
    Try(1);
    cout<<"The total solution number: "<<num<<endl;
    return 0;
}
